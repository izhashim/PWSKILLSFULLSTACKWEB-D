/*
Here are some common Browser APIs available to JavaScript developers:

DOM (Document Object Model) API: Allows JavaScript to interact with the structure and content of an HTML document. It provides methods to create, modify, and delete elements, as well as to traverse the document tree.

Geolocation API: Provides access to the user's location, allowing developers to create location-based applications.

Storage API: Allows developers to store and retrieve data locally on the client-side, using either localStorage or sessionStorage.

Web Storage API: Similar to the Storage API, but provides more advanced features, such as transactional storage and event-driven storage.

Web Sockets API: Enables bidirectional, real-time communication between the client and server, allowing for live updates and push notifications.

WebRTC (Web Real-Time Communication) API: Enables real-time communication, such as video conferencing and file transfer, between browsers.

Canvas API: Provides a 2D drawing surface, allowing developers to create dynamic graphics, animations, and games.

WebGL API: Provides a 3D drawing surface, allowing developers to create 3D graphics, animations, and games.

Fetch API: Provides a modern, promise-based way to make HTTP requests, replacing the older XMLHttpRequest API.

Web API: Provides access to various device features, such as the camera, microphone, and accelerometer, allowing developers to create device-aware applications.

Notification API: Allows developers to display notifications to the user, such as desktop notifications or push notifications.

Web Workers API: Enables developers to run scripts in the background, allowing for improved performance and responsiveness in web applications.

IndexedDB API: Provides a low-level, client-side storage system, allowing developers to store large amounts of structured data locally.

Web Cryptography API: Provides a set of cryptographic functions, allowing developers to perform tasks such as encryption, decryption, and digital signatures.

Screen Orientation API: Allows developers to control the screen orientation of a device, such as locking the screen to a specific orientation */