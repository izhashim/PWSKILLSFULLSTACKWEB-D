/*In JavaScript, operators are symbols that perform operations on operands. The number of operands an operator takes determines its arity. Here are the main types of operators based on arity:

Unary Operators:
Unary operators work with only one operand.
Example: */
let x = 5;
let y = -x; // Unary minus operator
console.log(y); // Output: -5
/*Common unary operators include:
+ (Unary plus)
- (Unary minus)
++ (Increment)
-- (Decrement)
! (Logical NOT)
typeof (Typeof) */

/*Binary Operators:
Binary operators work with two operands.
Example: */
let a = 5;
let b = 10;
let sum = a + b; // Binary plus operator
console.log(sum); // Output: 15
/*Common binary operators include:

+ (Addition)
- (Subtraction)
* (Multiplication)
/ (Division)
% (Modulus)
== (Equality)
!= (Inequality)
=== (Strict equality)
!== (Strict inequality)
> (Greater than)
< (Less than)
>= (Greater than or equal to)
<= (Less than or equal to)
&& (Logical AND)
|| (Logical OR)*/

/*Ternary Operator:
The ternary operator, also known as the conditional operator, is the only ternary operator in JavaScript.
It takes three operands and is used for conditional expressions.
Syntax: condition ? expression_if_true : expression_if_false
Example:*/
let age = 20;
let isAdult = age >= 18 ? "Yes" : "No";
console.log(isAdult); // Output: Yes
