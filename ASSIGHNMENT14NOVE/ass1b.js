/* 
Operators in JavaScript can be categorized based on their functionality into several groups. Here's a brief overview of these categories:

Arithmetic Operators:
Perform mathematical operations such as addition (+), subtraction (-), multiplication (*), division (/), and modulus (%).

Comparison Operators:
Compare values and return a Boolean result. Examples include equality (==, ===), inequality (!=, !==), greater than (>), less than (<), etc.

Logical Operators:
Perform logical operations on Boolean values. Include AND (&&), OR (||), and NOT (!).

Assignment Operators:
Assign values to variables. Examples include the simple assignment (=), addition assignment (+=), subtraction assignment (-=), etc.

Increment/Decrement Operators:
Increase (++) or decrease (--) the value of a variable by 1.

Bitwise Operators:
Perform bitwise operations on integer values. Include AND (&), OR (|), XOR (^), bitwise NOT (~), left shift (<<), right shift (>>), and unsigned right shift (>>>).

Conditional (Ternary) Operator:
A shorthand for an if-else statement. It has the form condition ? expr1 : expr2.

Typeof Operator:
Returns a string indicating the data type of an operand.

Instanceof Operator:
Checks whether an object is an instance of a particular class or constructor.

In Operator:
Checks if a property exists in an object.

Comma Operator:
Evaluates multiple expressions and returns the result of the last expression */